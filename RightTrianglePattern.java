
 public class RightTrianglePattern {
    
    public static void main(String[] args) {
        
        int[] marks = {100, 32, 66, 44, 66, 33, 22, 89 };

        for(int i=0; i<marks.length; i++){
            if(marks[i]<35)
              System.out.println(marks[i] + " ");
        }
        
    }
 }