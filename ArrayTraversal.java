class ArrayTraversal{
    public static void main(String [] args){
        int[] arr = {20, 30, 40, 50, 60};

        System.out.println("traversal number are");

        for(int i=0; i < arr.length; i++){
            System.out.println("number in the index :"+ i + " : " + arr[i]);
        }
    }
}